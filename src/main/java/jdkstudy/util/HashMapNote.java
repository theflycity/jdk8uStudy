package jdkstudy.util;

/**
 * HashMapNote
 *
 * 用于记录 JDK8 HashMap 的核心设计与源码阅读要点。
 * 目标：帮助理解数据结构设计、性能优化策略、以及源码阅读逻辑。
 *
 * =====================================
 * 一、HashMap 本质
 * =====================================
 *
 * JDK8 结构：
 *
 *      table (Node<K,V>[])
 *          │
 *          ├── Node -> Node -> Node       (链表)
 *          │
 *          └── TreeNode                   (红黑树)
 *
 * 即：
 *
 *      数组 + 链表 + 红黑树
 *
 * 设计目标：
 *
 * 1) O(1) 平均查找
 * 2) 冲突时仍保持较好性能
 * 3) 在极端情况下避免退化为 O(n)
 *
 * =====================================
 * 二、核心成员变量
 * =====================================
 */

import java.util.HashMap;

public class HashMapNote {

    /**
     * HashMap 内部最核心字段（概念记录）
     *
     * transient Node<K,V>[] table;
     *
     * 实际存储数据的数组。
     * 每个位置称为一个 bucket（桶）。
     */

    /**
     * transient int size
     *
     * 当前存储的 key-value 数量
     */

    /**
     * int threshold
     *
     * 扩容阈值
     *
     * threshold = capacity * loadFactor
     */

    /**
     * final float loadFactor
     *
     * 负载因子
     * 默认：0.75
     *
     * 含义：数组使用到 75% 时触发扩容
     */

    /**
     * transient int modCount
     *
     * 结构修改次数
     * 用于 fail-fast 机制
     */


    /*
     * =====================================
     * 三、容量必须是 2 的幂
     * =====================================
     *
     * HashMap 通过如下方式定位桶：
     *
     *      index = (n - 1) & hash
     *
     * 如果 n 为 2^k：
     *
     *      n = 16 = 10000
     *      n-1 = 15 = 01111
     *
     * 则：
     *
     *      hash & 15
     *
     * 等价于：
     *
     *      hash % 16
     *
     * 但位运算性能更高。
     */


    /*
     * =====================================
     * 四、tableSizeFor(cap)
     * =====================================
     *
     * 将任意容量转为 >=cap 的最小 2 的幂
     */

    static final int tableSizeFor(int cap) {
        int n = cap - 1;

        n |= n >>> 1;
        n |= n >>> 2;
        n |= n >>> 4;
        n |= n >>> 8;
        n |= n >>> 16;

        return (n < 0) ? 1 : (n >= (1 << 30)) ? (1 << 30) : n + 1;
    }


    /*
     * =====================================
     * 五、put() 核心流程
     * =====================================
     *
     * 1. 计算 hash
     *
     *      hash = key.hashCode() ^ (hash >>> 16)
     *
     * 目的：让高位参与运算
     *
     * 2. 定位桶
     *
     *      index = (n - 1) & hash
     *
     * 3. 桶为空
     *
     *      直接插入
     *
     * 4. 桶不为空
     *
     *      a) key 相同 → 覆盖
     *      b) key 不同 → 链表追加
     *
     * 5. 链表过长
     *
     *      >= 8
     *
     *      转红黑树
     */


    /*
     * =====================================
     * 六、树化条件
     * =====================================
     *
     * 链表长度 >= 8
     * 并且
     * table.length >= 64
     *
     * 否则优先扩容而不是树化。
     */


    /*
     * =====================================
     * 七、扩容 resize()
     * =====================================
     *
     * 触发条件：
     *
     *      size > threshold
     *
     * 默认：
     *
     *      capacity = 16
     *      loadFactor = 0.75
     *
     *      threshold = 12
     *
     * 第 13 个元素触发扩容。
     *
     * 扩容流程：
     *
     * 1. 新建 2 倍容量数组
     * 2. 重新分配旧元素
     */


    /*
     * =====================================
     * 八、JDK8 扩容优化
     * =====================================
     *
     * JDK7：
     *
     *      重新计算 hash
     *
     * JDK8：
     *
     *      只判断
     *
     *      (hash & oldCap) == 0
     *
     *      true  -> 原索引
     *      false -> 原索引 + oldCap
     *
     * 这样避免重新计算 hash。
     */


    /*
     * =====================================
     * 九、为什么不是线程安全
     * =====================================
     *
     * 多线程 put 可能导致：
     *
     * 1. 数据覆盖
     * 2. 链表环
     * 3. 数据丢失
     *
     * 并发场景应使用：
     *
     *      ConcurrentHashMap
     */


    /*
     * =====================================
     * 十、性能优化建议
     * =====================================
     *
     * 如果预计存储 N 个元素：
     *
     *      capacity ≈ N / 0.75
     *
     * 例如：
     *
     *      1000 个元素
     *
     *      new HashMap<>(1334)
     *
     * 避免 resize。
     */


    /*
     * =====================================
     * 十一、学习 HashMap 源码顺序
     * =====================================
     *
     * 推荐顺序：
     *
     * 1. 成员变量
     * 2. put()
     * 3. resize()
     * 4. treeifyBin()
     * 5. get()
     */


    public static void main(String[] args) {

        HashMap<String, String> map = new HashMap<>(3);

        map.put("A", "1");
        map.put("B", "2");
        map.put("C", "3");

        System.out.println(map);

        // 验证 tableSizeFor
        System.out.println("tableSizeFor(3) = " + tableSizeFor(3));
    }
}
